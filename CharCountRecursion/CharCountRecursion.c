int CountChar(char * str)
{	
	static int iCnt = 0;
	if(*str != '\0')
	{
		iCnt++;
		str++;
		CountChar(str);
	}
	return iCnt;
}

int main()
{
	char arr[20] = {'\0'};
	int iRet = 0;
	printf("Enter String\n");
	scanf(" %[^\n]",arr);

	iRet = CountChar(arr);
	printf("%d Characters.\n",iRet);

	return 0;
}

