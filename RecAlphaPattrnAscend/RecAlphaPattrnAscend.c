void DisplayPattern(int no)
{
        if(no > 1)
        {
        	no--;
            DisplayPattern(no);
            printf("%c  ",64+no);
        }
}


int main()
{
	int iValue = 0;
    printf("Enter number\n");
    scanf("%d",&iValue);
	DisplayPattern(iValue+1);

	return 0;
}

